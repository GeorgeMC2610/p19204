x = int(input("Εισάγετε έναν αριθμό --> "))

while True:
    print("Αριθμός εισόδου:", x)
    x *= 3
    x += 1
    print("Τριπλασιασμένος +1 αριθμός:", x)
    a = 0
    for i in str(x):
        a += int(i)
    x = a
    print("Αριθμός με προσθετημένα ψηφία:", x, "\n")
    if len(str(x)) == 1:
        break